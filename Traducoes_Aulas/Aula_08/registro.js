// Equivalente ao "registro Pessoa" do VisualG → objeto em JavaScript
let aluno = {
    nome: "",
    idade: 0,
    peso: 0.0
};

// Atribuições diretas (como no VisualG)
aluno.nome = "Joao";
aluno.idade = 18;
aluno.peso = 70.0;

// Exibição dos dados
console.log("Nome:", aluno.nome);
console.log("Idade:", aluno.idade);
console.log("Peso:", aluno.peso);