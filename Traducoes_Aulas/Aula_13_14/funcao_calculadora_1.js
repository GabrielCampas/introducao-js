// FUNCAO_CALCULADORA - Versão 1
// Função com parâmetros x e y, retornando a soma.

// A diferença entre função e procedimento
// é que a função tem um RETORNO.

function somar(x, y) {
    return x + y;
}

// Programa principal
console.log("O resultado é:", somar(10, 10));
console.log("O resultado é:", somar(2, 5));
console.log("O resultado é:", somar(7, 8));
